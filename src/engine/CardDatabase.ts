// ============================================
// 卡牌数据库导出
// ============================================

export const ALL_CARDS: Card[] = [...ATTACKER_CARDS, ...DEFENDER_CARDS];

export const CARD_DATABASE = {
  attacker: ATTACKER_CARDS,
  defender: DEFENDER_CARDS,
  all: ALL_CARDS,
};

// 按ID查找卡牌
export function getCardById(id: string): Card | undefined {
  return ALL_CARDS.find(card => card.id === id);
}

// 按代码查找卡牌
export function getCardByCode(code: string): Card | undefined {
  return ALL_CARDS.find(card => card.code === code);
}

// 按阵营和稀有度获取卡牌
export function getCardsByFactionAndRarity(faction: CardFaction, rarity: CardRarity): Card[] {
  return ALL_CARDS.filter(card => card.faction === faction && card.rarity === rarity);
}

// 按科技等级获取卡牌 (T0-T5)
export function getCardsByTechLevel(techLevel: number): Card[] {
  const startIndex = techLevel * 8;
  const endIndex = startIndex + 8;
  return [...ATTACKER_CARDS.slice(startIndex, endIndex), ...DEFENDER_CARDS.slice(startIndex, endIndex)];
}

export default CARD_DATABASE;
