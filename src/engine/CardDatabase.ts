// ============================================
// 本地类型定义
// ============================================

export type CardType = 'attack' | 'defense' | 'support';
export type CardRarity = 'common' | 'rare' | 'epic' | 'legendary';
export type CardFaction = 'attack' | 'defense';

export interface CardResources {
  compute: number;
  funds: number;
  information: number;
}

export interface CardEffect {
  type: string;
  value: number;
  resourceType?: string;
  immunityType?: string;
}

export interface Card {
  id: string;
  code: string;
  name: string;
  type: CardType;
  faction: CardFaction;
  rarity: CardRarity;
  cost: Partial<CardResources>;
  description: string;
  effects: CardEffect[];
  target: 'self' | 'opponent' | 'all';
  isAura?: boolean;
  winCondition?: {
    type: string;
    threshold: number;
  };
}

